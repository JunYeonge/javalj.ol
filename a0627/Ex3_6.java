public class Ex3_6 {
    public static void main(String[] args) {
        int a = 10;
        int b = 4;

        System.out.println();
        System.out.printf("%d + %d = %d%n", a, b, a + b); // %n = 줄바뀜
        System.out.printf("%d - %d = %d%n", a, b, a - b);
        System.out.printf("%d * %d = %d%n", a, b, a * b); 
        System.out.printf("%d / %d = %d", a, (float)b, a / (float)b); 
    }
}
